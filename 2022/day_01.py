# --------------------------------------------------------------------------- #
#    Day 1                                                                   #
# --------------------------------------------------------------------------- #

DAY = 1
EXAMPLE = False

# --------------------------------------------------------------------------- #
#    Preparation                                                              #
# --------------------------------------------------------------------------- #

file_name = f"2022/input/day_{DAY:0>2}"
if EXAMPLE:
    file_name += "_example"
file_name += ".csv"

# --------------------------------------------------------------------------- #
#    Reading input                                                            #
# --------------------------------------------------------------------------- #

from itertools import groupby

with open(file_name, "r") as file:
    calories = []
    for key, group in groupby(file, key=lambda line: line.strip() != ""):
        if key:
            calories.append(sum(map(int, group)))
    #print(calories)

# --------------------------------------------------------------------------- #
#    Part 1                                                                   #
# --------------------------------------------------------------------------- #
print("\nPart 1:")

print(max(calories))

# --------------------------------------------------------------------------- #
#    Part 2                                                                   #
# --------------------------------------------------------------------------- #
print("\nPart 2:")

print(sum(sorted(calories, reverse=True)[:3]))
