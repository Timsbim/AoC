import hashlib


DAY = 4
EXAMPLE = False

# --------------------------------------------------------------------------- #
#    Preparation                                                              #
# --------------------------------------------------------------------------- #
print("Day", DAY)

# --------------------------------------------------------------------------- #
#    Reading input                                                            #
# --------------------------------------------------------------------------- #

secret = "abcdef" if EXAMPLE else "bgvyzdsv"

# --------------------------------------------------------------------------- #
#    Helper                                                                   #
# --------------------------------------------------------------------------- #

def md5_hash(number):
    h = hashlib.new("md5")
    h.update(f"{secret}{number}".encode())
    return h.hexdigest()

# --------------------------------------------------------------------------- #
#    Part 1                                                                   #
# --------------------------------------------------------------------------- #
print("Part 1: ", end="")


def part_1():
    number = 0
    while True: 
        number += 1
        if md5_hash(number).startswith("00000"):
            return number


solution = part_1()
if not EXAMPLE:
    assert solution == 254575
print(solution)

# --------------------------------------------------------------------------- #
#    Part 2                                                                   #
# --------------------------------------------------------------------------- #
print("Part 2: ", end="")


def part_2():
    number = 0
    while True:
        number += 1
        if md5_hash(number).startswith("000000"):
            return number


solution = part_2()
if not EXAMPLE:
    assert solution == 1038736
print(solution)
