from pprint import pprint
from itertools import groupby


DAY = 10
EXAMPLE = False

# --------------------------------------------------------------------------- #
#    Preparation                                                              #
# --------------------------------------------------------------------------- #
print("Day", DAY)

# --------------------------------------------------------------------------- #
#    Reading input                                                            #
# --------------------------------------------------------------------------- #

digits = "1" if EXAMPLE else "3113322113"

# --------------------------------------------------------------------------- #
#    Helper                                                                   #
# --------------------------------------------------------------------------- #

def play(digits, rounds):
    digits = map(int, digits)
    for _ in range(rounds):
        digits = (
            n
            for digit, group in groupby(digits)
            for n in (len(list(group)), digit)
        )
    return sum(1 for _ in digits)

# --------------------------------------------------------------------------- #
#    Part 1                                                                   #
# --------------------------------------------------------------------------- #
print("Part 1: ", end="")

solution = play(digits, 5 if EXAMPLE else 40)
if not EXAMPLE:
    assert solution == 329356
print(solution)

# --------------------------------------------------------------------------- #
#    Part 2                                                                   #
# --------------------------------------------------------------------------- #
print("Part 2: ", end="")

solution = play(digits, 5 if EXAMPLE else 50)
if not EXAMPLE:
    assert solution == 4666278
print(solution)
