from math import floor, prod, sqrt


DAY = 6
EXAMPLE = False

# --------------------------------------------------------------------------- #
#    Preparation                                                              #
# --------------------------------------------------------------------------- #
print("Day", DAY)

file_name = f"2023/input/day_{DAY:0>2}"
if EXAMPLE:
    file_name += "_example"
file_name += ".csv"

# --------------------------------------------------------------------------- #
#    Reading input                                                            #
# --------------------------------------------------------------------------- #

with open(file_name, "r") as file:
    times = list(map(int, next(file).split()[1:]))
    distances = list(map(int, next(file).split()[1:]))
print(times)
print(distances)

# --------------------------------------------------------------------------- #
#    Helpers                                                                  #
# --------------------------------------------------------------------------- #

def breaks(time, distance):
    root = sqrt(time * time - 4 * distance)
    return floor((time + root) / 2) - int((time - root) / 2)

# --------------------------------------------------------------------------- #
#    Part 1                                                                   #
# --------------------------------------------------------------------------- #
print("Part 1: ", end="")

print(prod(breaks(t, d) for t, d in zip(times, distances)))  # 449550

# --------------------------------------------------------------------------- #
#    Part 2                                                                   #
# --------------------------------------------------------------------------- #
print("Part 2: ", end="")

time = int("".join(map(str, times)))
distance = int("".join(map(str, distances)))
print(breaks(time, distance))  # 28360140
